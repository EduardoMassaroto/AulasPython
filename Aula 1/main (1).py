# Exelente para loops dependentes de uma condicao.
# Criar uma promocao para um produto no valor de 150 reais

valor = 150
dia = 0

while valor > 50:
    dia += 1
    print(f' No dia {dia} o produto sera vendido por R$ {valor}')
    valor -= 10

